DROP TABLE IF EXISTS USER;

CREATE TABLE USER(
    USER_ID VARCHAR(45) IDENTITY PRIMARY KEY,
    USERS_NAME VARCHAR(45) NOT NULL,
    USERS_PASSWORD VARCHAR(45) NOT NULL,
    EMAIL VARCHAR(45) NOT NULL,
    AGE VARCHAR(45) NOT NULL
);

DROP TABLE IF EXISTS SONG;
CREATE TABLE SONG(
    SONG_ID VARCHAR(45) IDENTITY PRIMARY KEY,
    SONG_NAME VARCHAR(100) NOT NULL,
    SONG_ARTIST VARCHAR(45) NOT NULL,
    RELEASE_DATE DATETIME NOT NULL,
    ALBUM VARCHAR(45) NOT NULL,
    GENRE VARCHAR(45) NOT NULL
);

DROP TABLE IF EXISTS ALBUM;

CREATE TABLE ALBUM(
    ALBUM_ID VARCHAR(45) IDENTITY PRIMARY KEY,
    ALBUM_NAME VARCHAR(45) NOT NULL,
    ALBUM_ARTIST VARCHAR(45) NOT NULL,
    RELEASE_DATE DATETIME NOT NULL,
    TRACKS VARCHAR(45) NOT NULL
);
DROP TABLE IF EXISTS ARTIST;

CREATE TABLE ARTIST(
    ARTIST_ID VARCHAR(45) IDENTITY PRIMARY KEY,
    ARTIST_NAME VARCHAR(45) NOT NULL,
    GENRES VARCHAR(45) NOT NULL,
    IMAGES VARCHAR(45) NOT NULL
);

DROP TABLE IF EXISTS EPISODE;

CREATE TABLE EPISODE(
    EPISODE_ID VARCHAR(45) IDENTITY PRIMARY KEY,
    EPISODE_NAME VARCHAR(45) NOT NULL,
    PARTICIPANTS_NAME VARCHAR(45) NOT NULL,
    IMAGES VARCHAR(45) NOT NULL,
    TRACKS VARCHAR(45) NOT NULL
);